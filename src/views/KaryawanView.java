    public void bindingTable() {
        String[] header = {"KD Karyawan", "Nama Karyawan", "Alamat", "Telepon", "Password"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(header, 0);
        for (Karyawan karyawan : karyawanController.bindingsort("kd_karyawan", "asc")) {
            //for (Region region : regionController.binding("region_id","asc")) {
            Object[] location1 = {karyawan.getKdKaryawan(), karyawan.getNamaaKaryawan(),
                karyawan.getAlamat(), karyawan.getTelepon(), karyawan.getPassword()
            };
            defaultTableModel.addRow(location1);
        }
        tblKaryawan.setModel(defaultTableModel);
    }
     public void searchTable(String category, String data) {
        String[] header = {"KD Karyawan", "Nama Karyawan", "Alamat", "Telepon", "Password"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(header, 0);
        for (Karyawan karyawan : karyawanController.find(category, data)) {
            Object[] location1 = {
                karyawan.getKdKaryawan(), karyawan.getNamaaKaryawan(),
                karyawan.getAlamat(), karyawan.getTelepon(), karyawan.getPassword()
            };
            defaultTableModel.addRow(location1);
        }
        tblKaryawan.setModel(defaultTableModel);
    }
    public void reset() {
        textKd.setText("");
        textNama.setText("");
        textAlamat.setText("");
        textTelepon.setText("");
        textPassword.setText("");      
    }
